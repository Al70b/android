    private EmojiconHandler() {
    }

    /**
     * Convert emoji characters of the given Spannable to the according emojicon.
     *
     * @param context
     * @param text
     * @param emojiSize
     */
    public static void addEmojis(Context context, Spannable text, int emojiSize) {
        addEmojis(context, text, emojiSize, 0, -1);
    }

    /**
     * Convert emoji characters of the given Spannable to the according emojicon.
     *
     * @param context
     * @param text
     * @param emojiSize
     * @param index
     * @param length
     */
    public static void addEmojis(Context context, Spannable text, int emojiSize, int index, int length) {

        Integer icon;
        Matcher matcher = SHORTNAME_PATTERN.matcher(text);

        while (matcher.find()) {
            String emojiconStr = matcher.group();


            icon = sEmojisMap.get(emojiconStr);

            if (icon != null && icon.intValue() > 0) {
                text.setSpan(new EmojiconSpan(context, icon, emojiSize), matcher.start(), matcher.start() + emojiconStr.length(), Spannable.SPAN_EXCLUSIVE_EXCLUSIVE);
            }
        }
    }

